if (temperature < 0) {
    console.log('Just stay inside'); } 
    
else if (temperature < 30) {
    console.log('Put on your coat and hat'); } 
 
else if (temperature < 50) {
    console.log('Put on your coat'); }


else if (temperature > 50 && temperature === 55)  {
    console.log('It is very hot'); }

